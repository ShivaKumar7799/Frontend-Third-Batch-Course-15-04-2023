// round method 

let petrolPrice = 112.12;

// round will give the nearest integer value 

console.log(Math.round(petrolPrice), "round")

// floor will give the previous integer value 

console.log(Math.floor(petrolPrice), "floor");

// ceil method will give the next integer value

console.log(Math.ceil(petrolPrice), "ceil")

// if we want to calculate the square of a number - pow

console.log(Math.pow(2,5), "power of a number")

// square root - math.sqrt

console.log(Math.sqrt(144))


const vizag = 0;
const hyderabad = 600

// abs method will always give the positive value

const totalDistance = vizag - hyderabad;

console.log(Math.abs(totalDistance))


// random method 

var dice = Math.ceil(Math.random() * 90)

console.log(dice)

// max an min number

console.log(Math.max(2,4,638,4,2,12,534,34,343,24))

console.log(Math.min(2,4,63,4,2,1,534,34,343,24))


// if we want to convert a string to a number - Number

const laptopPrice = "46546";
console.log(laptopPrice, typeof laptopPrice)
console.log(Number(laptopPrice), typeof Number(laptopPrice))

// converting number to string - toString()

const bike = 565656;
console.log(bike, typeof bike)
console.log(bike.toString(), typeof bike.toString())

// parseInt will give the output in integers;

const keyboardPrice = "313.45";

console.log(parseInt(keyboardPrice), typeof parseInt(keyboardPrice) )  // parseInt will convert a string tonumber and will remove the decimal points

console.log(parseFloat(keyboardPrice), typeof parseFloat(keyboardPrice))





